/** @param {import("highlight.js").HLJSApi} hljs */
function defineDax(hljs) {
  const NUMBER = {
    className: "number",
    variants: [{ begin: /\b\d+(?:\.\d+)?(?:[eE][+-]?\d+)?\b/ }],
    relevance: 0,
  };

  const STRING = {
    className: "string",
    begin: /"/,
    end: /"/,
    contains: [{ begin: /""/, relevance: 0 }],
  };

  const LINE_COMMENT = {
    className: "comment",
    variants: [hljs.C_LINE_COMMENT_MODE, { begin: /--/, end: /$/ }],
    relevance: 0,
  };

  const QUOTED_TABLE = {
    className: "symbol",
    begin: /'[^']+'(?=\[)/,
    relevance: 0,
  };

  const UNQUOTED_TABLE = {
    className: "symbol",
    begin: /[A-Za-z_][A-Za-z0-9_]*(?=\[)/,
    relevance: 0,
  };

  const MEASURE_REF = {
    className: "variable",
    begin: /\[[^\]]+\]/,
    relevance: 0,
  };

  return {
    name: "DAX",
    aliases: ["dax"],
    case_insensitive: true,
    keywords: {
      keyword: DAX_KEYWORDS,
      built_in: DAX_FUNCTIONS,
      literal: DAX_LITERALS,
    },
    contains: [
      hljs.C_BLOCK_COMMENT_MODE,
      LINE_COMMENT,
      STRING,
      QUOTED_TABLE,
      UNQUOTED_TABLE,
      MEASURE_REF,
      NUMBER,
    ],
  };
}

/** @type {import("highlight.js").LanguageFn} */
function register(hljs) {
  return defineDax(hljs);
}

export const dax = { name: "dax", register };
export default dax;
